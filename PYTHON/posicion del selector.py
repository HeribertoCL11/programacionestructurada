selector = int(input("Ingrese la posición del selector  "))

if selector == 1:
    print("Entrada 1 seleccionada")
elif selector == 2:
    print("Entrada 2 seleccionada")
elif selector == 3:
    print("Entrada 3 seleccionada")
elif selector == 4:
    print("Entrada 4 seleccionada")
else:
    print("Valor inválido")
